# > 표시는 오른쪽 줄맞춤을 지정한다.

s1 = 'a'
s2 = 'ab'
s3 = 'abc'
s4 = 'abcd'

print("\n > 표시는 오른쪽 줄맞춤을 지정한다.")
print(f'{s1:>10}')
print(f'{s2:>10}')
print(f'{s3:>10}')
print(f'{s4:>10}')

print("\n < 표시는 왼쪽 줄맞춤을 지정한다.")
