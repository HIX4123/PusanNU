print("\n 너비(width) 지정자없이 그냥 찍기(왼쪽 맞춤)")
for x in range(1, 31, 3):
    print(f'{x} {x*x} {x*x*x}')

print("\n 너비(width) 지정자를 활용해서오른쪽 맞추기")

for x in range(1, 31, 3):
    print(f'{x:03} : {x*x:7} {x*x*x:10}')