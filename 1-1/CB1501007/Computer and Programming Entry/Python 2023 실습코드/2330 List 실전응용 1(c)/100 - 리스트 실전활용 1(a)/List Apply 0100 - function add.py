fa=[ 3, 4, 2, 0, 0 -3, 1] # f(x)=3 + 4x + 2x^2 - 3x5 + x^6
fb=[-3, 5, 1, 0, 4]       # g(x) = -3 + 5x + x^2 + x^4

fab=[]                    # fab(x) = fa(x) + fb(x)

for ca, cb in zip(fa, fb):
    fab.append( ca + cb)

print(fab)
