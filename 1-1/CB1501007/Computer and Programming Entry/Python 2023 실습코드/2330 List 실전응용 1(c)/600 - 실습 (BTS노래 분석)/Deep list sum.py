def listsum( L ) :
    if type(L) == int :
        print("L=", L)
        return( L )

    if len(L) == 0 :
        return(0)

    psum = 0
    for w in L :
        if type(w) == list :
            psum += listsum(w)
        else :
            psum += w

    return( psum )


