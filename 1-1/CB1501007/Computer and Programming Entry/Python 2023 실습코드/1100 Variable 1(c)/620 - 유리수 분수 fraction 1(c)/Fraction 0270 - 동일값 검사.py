import fractions


a= fractions.Fraction(11111/33334)
b= fractions.Fraction(22222/66668)

if a == b : print("a == b")
else :      print("a != b")


p = 11111/33334
q = 22222/66668
print(f"p={p}, q={q}")

if  p == q  :  print(" p == q 같음")
else :         print(" p != q 다르네요")

