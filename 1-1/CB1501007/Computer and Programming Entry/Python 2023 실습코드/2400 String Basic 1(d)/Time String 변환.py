def When( Exstr ):
    Exlist = Exstr.split()
    Dlist = list( map(int, Exlist[0].split('-') ) )
    Tlist = list( map(int, Exlist[1].split(':') ) )
    Dlist.extend( Tlist)
    return( Dlist )


