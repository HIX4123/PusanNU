def custom_key(x):
   return -x[1], x[0]     # 두번째 원소가 큰 것, 그 다음 첫번ㅉ 원소 오름차수

l = [('betty', 1), ('a', 1), ('but', 2), ('butter', 2), ('butter', 3)]
l.sort(key=custom_key)

print(l)