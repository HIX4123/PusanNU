import re

# match를 사용하면 좋은 예

num=[ '010-4567-9878', '016-876-2345', '010-123-45678', '016-012-3478',\
      '4567-5434', '010-122-8912', '010-34-5637', '016-345-9019']


sphone=r'(010|016)-[1-9]\d{2,3}-\d{4}'

for w in num :
    if re.match( sphone, w) :
        print( f'>> {w} , Valid')
    else :
         print( f'>> ........ {w} , Invalid')